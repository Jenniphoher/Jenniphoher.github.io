 function slideReact() {
    var info = document.getElementById("in");
    var sch = document.getElementById("sc");
    var wrk = document.getElementById("wr");
    var ext = document.getElementById("ex");
    info.style.visibility = "visible";
    
    if(info.style.visibility = "visible") {
      sch.style.visibility = "hidden";
      wrk.style.visibility = "hidden";
      ext.style.visibility = "hidden";
    }
  }

